#include <stdio.h>

int main()
{

    char caractere = 'A';
    unsigned char uchar = 200;
    int inteiro = 42;
    unsigned int uint = 1000;
    float pontoFlutuante = 3.14;
    double duplo = 12345.6789;

    printf("Char: %c\n", caractere);
    printf("Unsigned Char: %u\n", uchar);
    printf("Inteiro: %d\n", inteiro);
    printf("Unsigned Int: %u\n", uint);
    printf("Ponto Flutuante: %f\n", pontoFlutuante);
    printf("Duplo: %lf\n", duplo);

    return 0;
}